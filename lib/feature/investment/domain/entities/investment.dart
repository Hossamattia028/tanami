class Investment {  
  final int? id;  
  final String image;  
  final String houseNumber;  
  final String street;  
  final String state;  
  final String zipCode;  
  final String country;  

  const Investment({  
    this.id,  
    required this.image,  
    required this.houseNumber,  
    required this.street,  
    required this.state,  
    required this.zipCode,  
    required this.country,  
  });  
}  